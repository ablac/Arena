import assert from 'node:assert/strict';
import { createHash } from 'node:crypto';
import { mkdir, readFile, readdir, unlink, writeFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import { build } from 'esbuild';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const entry = resolve(root, 'frontend/vendor-src/babylon-runtime-entry.js');
const vendorDir = resolve(root, 'frontend/assets/vendor');
const bridgePath = resolve(root, 'frontend/js/babylon-runtime.js');
const checkOnly = process.argv.includes('--check');

const result = await build({
  entryPoints: [entry],
  bundle: true,
  format: 'iife',
  platform: 'browser',
  target: ['es2020'],
  minify: true,
  legalComments: 'eof',
  banner: {
    js: '/*! @babylonjs/core 9.14.0 (Apache-2.0) | earcut 2.2.4 (ISC) */',
  },
  write: false,
});

const runtime = result.outputFiles[0].contents;
const hash = createHash('sha256').update(runtime).digest('hex').slice(0, 12);
const filename = `babylon-runtime.${hash}.min.js`;
const assetPath = resolve(vendorDir, filename);
const bridge = `// Generated by scripts/build-babylon-runtime.mjs.\nimport '../assets/vendor/${filename}';\n\nif (!globalThis.BABYLON) {\n  throw new Error('Arena Babylon runtime failed to initialize');\n}\n\nexport default globalThis.BABYLON;\n`;

if (checkOnly) {
  assert.equal(Buffer.compare(await readFile(assetPath), Buffer.from(runtime)), 0,
    `${filename} is stale; run npm run build:babylon`);
  assert.equal(await readFile(bridgePath, 'utf8'), bridge,
    'frontend/js/babylon-runtime.js is stale; run npm run build:babylon');
  console.log(`Babylon runtime is reproducible: ${filename} (${runtime.byteLength} bytes)`);
} else {
  await mkdir(vendorDir, { recursive: true });
  for (const stale of await readdir(vendorDir)) {
    if (/^babylon-runtime\.[a-f0-9]{12}\.min\.js$/.test(stale) && stale !== filename) {
      await unlink(resolve(vendorDir, stale));
    }
  }
  await writeFile(assetPath, runtime);
  await writeFile(bridgePath, bridge);
  console.log(`Wrote ${filename} (${runtime.byteLength} bytes)`);
}
